/**
* @brief This function handles TIM1 update interrupt and TIM10 global interrupt.
*/

volatile int32_t calibra_adc_ax= 0;
volatile int16_t calibra_adc;
uint16_t startup;
uint16_t cx;

float y;
float r;
float u;
float e;
float esum;



void TIM1_UP_TIM10_IRQHandler(void) //Tarefa em tempo real é escrita aqui (esta função é chamada a cada 1/20KHz
{


	if(startup<10)
	{
		startup++; // Aguarda inicialização do conversor ADC
	} else if (startup<1010) {
		startup++;
		calibra_adc_ax=calibra_adc_ax+valoradc_I;
		calibra_adc = (uint16_t)(calibra_adc_ax/1000); // Faz a média das primeiras 1000 medidas para retirar o offset depois

	} else {


		y = -(((float)valoradc_I)-(float)calibra_adc)/754.8; // Tira o offset e ajusta o ganho


		cx++; // contador para gerar uma onda triangular
		if(cx==3040) //era 2170
		{
			cx=0;
		}
		r = 0.2*cosf(cx*11*0.019); // gera uma referência senoidal

		rtU.r = r; // passa os argumentos para a função do MATLAB
		rtU.y = y;

		MATLAB_step(); // executa um step da função do MATLAB

		u = rtY.u; // pega o valor de volta da função MATLAB




		HAL_DAC_SetValue(&hdac, DAC_CHANNEL_1, DAC_ALIGN_12B_L, (uint32_t)(fabsf((y)*7000+5000)));  //permite observar o valor das variáveis no DAC
		HAL_DAC_SetValue(&hdac, DAC_CHANNEL_2, DAC_ALIGN_12B_L, (uint32_t)(fabsf((r)*7000+5000)));




	}


	HAL_TIM_IRQHandler(&htim1); // Devolve o controle da interrupção

  /* USER CODE BEGIN TIM1_UP_TIM10_IRQn 1 */

  /* USER CODE END TIM1_UP_TIM10_IRQn 1 */
}

